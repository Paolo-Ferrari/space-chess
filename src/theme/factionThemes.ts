import type { FactionTheme } from "./types";

function theme(
  partial: Omit<FactionTheme, "glowSoft" | "accentDim" | "glassBorder"> &
    Partial<Pick<FactionTheme, "glowSoft" | "accentDim" | "glassBorder">>,
): FactionTheme {
  return {
    accentDim: partial.accentDim ?? "#2a2a2a",
    glowSoft: partial.glowSoft ?? partial.accentSoft,
    glassBorder: partial.glassBorder ?? partial.panelBorder,
    ...partial,
  };
}

